# Typecasting a string to an integer
string_number = "42"
integer_number = int(string_number)
# Display the result
print(f"String: {string_number}, Integer: {integer_number}")


# Typecasting an integer to a string
integer_value = 42
string_value = str(integer_value)
# Display the result
print(f"Integer: {integer_value}, String: {string_value}")

# Typecasting a string to a float
string_float = "3.14"
float_number = float(string_float)
# Display the result
print(f"String: {string_float}, Float: {float_number}")

# Typecasting a float to an integer
float_number = 3.14
integer_number = int(float_number)
# Display the result
print(f"Float: {float_number}, Integer: {integer_number}")

# Typecasting an integer to a boolean
integer_value = 0
boolean_value = bool(integer_value)
# Display the result
print(f"Integer: {integer_value}, Boolean: {boolean_value}")


