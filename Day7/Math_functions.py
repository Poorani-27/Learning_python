import math

# Absolute Value
x = -5
absolute_value = abs(x)

# Ceiling Function
y = 5.3
ceil_value = math.ceil(y)

# Exponentiation
base = 2
exponent = 3
power_result = base ** exponent

# Floor Function
z = 5.8
floor_value = math.floor(z)

# Natural Logarithm
log_value = math.log(10)

# Base 10 Logarithm
log10_result = math.log10(100)

# Maximum Value
m1, m2, m3 = 2, 5, 8
max_value = max(m1, m2, m3)

# Minimum Value
min_value = min(m1, m2, m3)

# Modulus
x = 10
y = 3
modulus_result = x % y

# Rounding
round_value = round(3.14159, 2)

# Print results
print("Absolute Value:", absolute_value)
print("Ceiling Value:", ceil_value)
print("Exponentiation Result:", power_result)
print("Floor Value:", floor_value)
print("Natural Logarithm:", log_value)
print("Base 10 Logarithm:", log10_result)
print("Maximum Value:", max_value)
print("Minimum Value:", min_value)
print("Modulus Result:", modulus_result)
print("Rounded Value:", round_value)
